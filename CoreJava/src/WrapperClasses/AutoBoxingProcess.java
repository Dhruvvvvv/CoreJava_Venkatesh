package WrapperClasses;

public class AutoBoxingProcess {

	public static void main(String[] args) {
		System.out.println("=======Performing Auto Boxing =========");
		Integer ob1= 12;
		Character ob2= 'A';
		Boolean ob3= false;
		Float ob4= 234.34F;
		System.out.println("After performing autboxing");
		System.out.println("Object1: "+ob1);
		System.out.println("Object2: "+ob2);
		System.out.println("Object3: "+ob3);
		System.out.println("Object4: "+ob4);

	}

}
