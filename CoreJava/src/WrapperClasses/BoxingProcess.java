package WrapperClasses;

public class BoxingProcess {

	public static void main(String[] args) {
		Integer obj1 = Integer.valueOf(12);
		Character obj2 = Character.valueOf('A');
		Float obj3 = Float.valueOf(123.4543F);
		Boolean obj4 = Boolean.valueOf(false);
		System.out.println("=========Objects=======");
		System.out.println("OBJECT1: " + obj1);
		System.out.println("OBJECT2: " + obj2);
		System.out.println("OBJECT3: " + obj3);
		System.out.println("OBJECT4: " + obj4);

	}

}
